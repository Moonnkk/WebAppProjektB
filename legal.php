// Legal page tab switching
document.addEventListener('DOMContentLoaded', function() {
  var tabs = document.querySelectorAll('.legal-tab');
  var sections = document.querySelectorAll('.legal-section');

  // Check URL parameter
  var urlParams = new URLSearchParams(window.location.search);
  var targetSection = urlParams.get('section');
  if (targetSection) {
    tabs.forEach(function(t) { t.classList.remove('active'); });
    sections.forEach(function(s) { s.classList.remove('active'); });
    var targetTab = document.querySelector('[data-target="' + targetSection + '"]');
    var targetEl = document.getElementById(targetSection);
    if (targetTab) targetTab.classList.add('active');
    if (targetEl) targetEl.classList.add('active');
  }

  tabs.forEach(function(tab) {
    tab.addEventListener('click', function() {
      tabs.forEach(function(t) { t.classList.remove('active'); });
      sections.forEach(function(s) { s.classList.remove('active'); });
      this.classList.add('active');
      var target = document.getElementById(this.getAttribute('data-target'));
      if (target) target.classList.add('active');
    });
  });
});
</script>

